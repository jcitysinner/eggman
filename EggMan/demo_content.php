<h2>HTML Tags</h2>

	<p>These supported tags come from the WordPress.com code <a href=
	"http://en.support.wordpress.com/code/" title="Code">FAQ</a>.</p>
	
	<p><strong>Address Tag</strong></p>

	<address>
	  1 Infinite Loop<br>
	  Cupertino, CA 95014<br>
	  United States
	</address>
	
	<p><strong>Anchor Tag (aka. Link)</strong></p>
	
	<p>This is an example of a <a href="http://apple.com" title=
	"Apple">link</a>.</p>
	
	<p><strong>Abbreviation Tag</strong></p>
	
	<p>The abbreviation <abbr title="Seriously">srsly</abbr> stands for
	“seriously”.</p>
	
	<p><strong>Acronym Tag (<em>deprecated in HTML5</em>)</strong></p>
	
	<p>The acronym <acronym title="For The Win">ftw</acronym> stands for “for the
	win”.</p>
	
	<p><strong>Big Tag&nbsp;</strong>(<em>deprecated in HTML5</em>)</p>
	
	<p>These tests are a <big>big</big> deal, but this tag is no longer supported
	in HTML5.</p>
	
	<p><strong>Cite Tag</strong></p>
	
	<p>“Code is poetry.” –<cite>Automattic</cite></p>
	
	<p><strong>Code Tag</strong></p>
	
	<p>You will learn later on in these tests that <code>word-wrap:
	break-word;</code> will be your best friend.</p>
	
	<p><strong>Delete Tag</strong></p>
	
	<p>This tag will let you <del>strikeout text</del>, but this tag is no longer
	supported in HTML5 (use the <code>&lt;strike&gt;</code> instead).</p>
	
	<p><strong>Emphasize Tag</strong></p>
	
	<p>The emphasize tag should <em>italicize</em> text.</p>
	
	<p><strong>Insert Tag</strong></p>
	
	<p>This tag should denote <ins>inserted</ins> text.</p>
	
	<p><strong>Keyboard Tag</strong></p>
	
	<p>This scarsly known tag emulates <kbd>keyboard text</kbd>, which is usually
	styled like the <code>&lt;code&gt;</code> tag.</p>
	
	<p><strong>Preformatted Tag</strong></p>

	<p>This tag styles large blocks of code.</p>
	<pre>
	.post-title {
	    margin: 0 0 5px;
	    font-weight: bold;
	    font-size: 38px;
	    line-height: 1.2;
	    and here's a line of some really, really, really, really long text, just to see how the PRE tag handles it and to find out how it overflows;
	}
	</pre>
	
	<p><strong>Quote Tag</strong></p>
	
	<p><q>Developers, developers, developers…</q> –Steve Ballmer</p>
	
	<p><strong>Strike Tag&nbsp;</strong>(<em>deprecated in HTML5</em>)</p>
	
	<p>This tag shows <span style="text-decoration:line-through;">strike-through
	text</span></p>
	
	<p><strong>Strong Tag</strong></p>
	
	<p>This tag shows <strong>bold</strong> text.</p>
	
	<p><strong>Subscript Tag</strong></p>
	
	<p>Getting our science styling on with H<sub>2</sub>O, which should push the
	“2″ down.</p>
	
	<p><strong>Superscript Tag</strong></p>
	
	<p>Still sticking with science and Isaac Newton’s E = MC<sup>2</sup>, which
	should lift the 2 up.</p>
	
	<p><strong>Teletype Tag&nbsp;</strong>(<em>deprecated in HTML5</em>)</p>
	
	<p>This rarely used tag emulates <tt>teletype text</tt>, which is usually
	styled like the <code>&lt;code&gt;</code> tag.</p>
	
	<p><strong>Variable Tag</strong></p>
	
	<p>This allows you to denote <var>variables</var>.</p>
	
